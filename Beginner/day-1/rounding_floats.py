"""
THE ROUND FUNCTION: print(round(8/3))  # rounds to a whole number
print(round(8/3, 2)) # rounds to two decimal places

THE FLOOR DIVISION OPERATOR: //
print(8 // 3)  # returns an integer without the decimals
"""

# ROUNDING VALUES
print(round(2.884123, 3))  # round to 3 decimal places
print(type(round(8 / 3)))  # rounds to a whole number (integer)

# FLOOR DIVISION
print(7 // 2)  # returns a whole number (an integer)

# USING SHORT HAND OPERATORS
result = 4 / 2
result /= 2
print(result)