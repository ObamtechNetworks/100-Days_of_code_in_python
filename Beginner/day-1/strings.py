"""
Objective:
	Learn about the string data type and also how to get characters from
 strings using SUBSCRIPTING (square brackets [index] with index)
"""

# SUBSCRIPTING STRINGS
print("Hello"[0])
# --- output -> H

# getting the last value of a str
print("My String"[-1])

# USING THE SPLICING OPERATION
print("My string"[0:2])  # gets first and second char